                        <span class="caption-subject font-blue-sharp bold uppercase">What is a Trading Plan?</span>
                    </div>
                </div>
                <div class="portlet-body">
                     <p>Every new trader in every asset class enters the market with one main goal: buy low and sell high in order to make a profit. This sounds simple enough, and is basically what we are trying to do, but the idiosyncracies of the market make it much more difficult than it seems at first glance. This is the main reason why many successful traders base their strategies on historical time and price data, otherwise know as charts. These charts allow us to perform what is called “technical analysis” on financial markets, a method that attempts to separate fundamental noise from true supply and demand action.</p>

                     <p>Over years of trial and error in markets, traders realized that a detailed “trading plan” to guide investment strategy is the best way to minimize emotional involvement, as well as give a solid foundation for comparative analysis and optimization. Essentially what this entails is laying out a set of guidelines that can be broadly applied to the markets you are trading, but that contain very specific rules for entering, exiting, and managing trades.</p>

                     <p>Below we have created a sample trading plan that can be used as a template for navigating the choppy altcoin waters. This should not be viewed as a comprehensive roadmap to profit, but rather as a starting point and initial criterion for traders that want to develop their strategy within this framework.</p>
