                   </div>
               </div>
               <div class="portlet-body">
                    <p>While there will be many similarities between legacy market trading plans and crypto market trading plans, especially in structure, there are some key differences that we want to outline before diving into the nitty gritty. First of all, crypto markets are young and highly illiquid markets which implies that volatility is orders of magnitude greater than in those traditional markets (such as stocks, bonds, and indices).</p>

                    <p>Additionally, these markets can be subject to manipulation by large position holders (aka whales), so having a firm grasp on the technical picture is often more valuable than knowing the details of the fundamentals. And lastly, due to the experimental nature of these projects we think it is paramount to speculate only with funds that can be lost in their entirety. This is just proper risk management.</p>

                    <p>Other than these few key factors, trading the cryptocurrency markets is just as legitimate as trading the legacy markets, but with much greater profit potential for prudent and selective participants.</p>
