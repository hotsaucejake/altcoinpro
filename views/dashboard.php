                   <p>As we previously mentioned, the main goal in these market will be to buy low and sell high so that is what we are basing our trading strategy on. Not only that, but we are looking for assets that have been under­owned and underappreciated by traders in the recent past, as well as those that exhibit signs of a newly emerging bull market.</p>

                   <p>The rules listed below will assist in finding and executing trades in these markets, however there is no substitute for hard work and research. There are potentially hundreds of coins to choose from so combing through them one by one and identifying those that fit our criteria is a large part of our potential success in these markets.</p>

                   <p>Once we have identified the assets we are interested in, then we can start strictly applying our trading criteria in order to glean optimal entry and exit areas for each setup. Despite the fact that this may all sound easy enough, in real trading conditions things get much more complicated. Remember, this will take alot of practice, but the rewards last a lifetime!</p>
